import math


def main(x, z):
    s = 0
    n = len(x)
    for i in range(1, n + 1):
        i3 = math.ceil(i / 3)
        i4 = math.ceil(i / 4)
        s += 56 * (36 * x[n - i3] ** 2 - z[n - i4] ** 3 - z[i3 - 1]) ** 7
    return s


main([0.16, -0.19, -0.7, 0.08, -0.96, -0.73],
     [0.33, 1.0, -0.35, -0.56, -0.13, -0.69])
main([1.0, -0.96, -0.83, 0.41, -0.45, -0.74],
     [0.15, -0.24, 0.96, 0.83, -0.91, -0.15])

